///find month name from month number;
var monthNumber = 4;
var monthName = '';

switch(monthNumber) {
    case 1:
        monthName:"January";
        break;
    case 2: 
        monthName:"February";
        break;
    case 3:
        monthName:"March";
        break;
    case 4:
        monthName:"April";
        break;
    case 5:
        monthName:"May";
        break;
   
     default:
        console.log('Invalid Choice');

}
console.log(monthName);
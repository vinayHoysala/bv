// find day name from day number
var  dayNumber = 3;
var dayName ='';

switch(dayNumber) {
    case 1:
        dayName ="Sunday";
        break;
    case 2:
        dayName ="Monday";
        break;
    case 3:
        dayName = "Tuesday";
        break;
    case 4:
        dayName = "wednesday";
        break;
    default:
        console.log('Invalid Choice');


}
console.log(dayName);





